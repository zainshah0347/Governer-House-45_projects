//Name Cases
var personNamess = "Eric";
// Print name in lowercase
console.log("Lowercase: ".concat(personNamess.toLowerCase()));
// Print name in uppercase
console.log("Uppercase: ".concat(personNamess.toUpperCase()));
// Print name in titlecase
console.log("Titlecase: ".concat(personNamess.charAt(0).toUpperCase()).concat(personNamess.slice(1).toLowerCase()));
