//Name Cases

let personNamess: string = "Eric";

// Print name in lowercase
console.log(`Lowercase: ${personNamess.toLowerCase()}`);

// Print name in uppercase
console.log(`Uppercase: ${personNamess.toUpperCase()}`);

// Print name in titlecase
console.log(`Titlecase: ${personNamess.charAt(0).toUpperCase()}${personNamess.slice(1).toLowerCase()}`);
